// Getting input via STDIN
const readline = require("readline");

const inp = readline.createInterface({
  input: process.stdin
});

const userInput = [];

inp.on("line", (data) => {
  userInput.push(data);
});

inp.on("close", () => {
    let n = +userInput[0];
    let arr = userInput[1].split(' ');
    
    let ans = arr.filter((x,idx)=> arr.indexOf(x)===idx);
    console.log(ans.join(' '));
});